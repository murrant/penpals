<p>P.S. apparently are the most read comments - this is where i tell them his audiobook is FREE on youtube. Consider adding something fun in a P.S. note
</p>
<ol start="1">
    <li>Include customizable flyers from <a href="https://yangcurious.com">https://yangcurious.com</a>.
        This is a huge resource, as it's highly customizable for the type of content you want as well as the area
        you're writing! More printables on <a href="https://yangprints.com">YangPrints.com</a>
        Just be sure not to use anything too content-dense, because that's the best way to make someone's eyes gloss over
        and they may just outright ignore you.
    </li>
    <li>Tell them how you got Yanged. They don't want just mindless stats and pushy rhetoric. They will engage with what you have to say if you put some of yourself into it.
    </li>
    <li>Include whatever you are comfortable with and anything extra that you may want. Yang bucks, holiday cards, and campaign stickers are all good options. If you can afford it,
        consider sending an envelope with a stamp for them to reply to you with or include an email address that they
        can contact you with- you can make one just for this. You could also send them here!
    </li>
    <li>Keep in mind the area you're writing to. Different places have different needs and concerns. Do a bit of research on the town and its needs, and see if any relate to your
        community. Elaborate on why you think yang will help you both.
    </li>
    <li>If you have the skill (or not!), add art and make it look like you really put time into it. Because we're sure you did! Make it pretty. Make it jump out at them, and let
        them know it's not just random junk mail.
    </li>
