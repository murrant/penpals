<p>The Basic Guidelines for letter writing !! </p>
<p>First, let's tackle what NOT to do. That is much easier than trying to list everything you should do! So, avoid these things when writing your letters:
</p>
<ol start="1">
    <li>Don't tear down or ridicule other candidates or campaigns. We are here to spread the humanity and reach out, not to discredit others with the tactics they use. we're better
        than that. Stick to the facts and the policies!
    </li>
    <li>Try not to be political with your envelope or address to the recipient. By this we mean make an attempt to avoid 'fellow voter' in place of a name, putting lots of campaign
        stickers on the envelope, etc. These things are best included inside the envelope because people so often throw out political mailers, we don't want your letters to get
        trashed before they are even opened! &nbsp;
    </li>
    <li>Please don't claim to be officially affiliated with the Yang2020 campaign. We aren't affiliated, and that is actually our greatest strength - imagine the passionate support
        of people you never even asked it from! It's a powerful thing to be able to move people to action without paying them or the promise of an immediate reward, and the people
        we're reaching out to know that.
    </li>
    <li>Don't copy other people's letters, and try not to form-letter everyone. Understandably, some of us have problems writing out letter after letter by hand. If you do decide
        to type, at least make sure to add some personal touches like a note with a Yang sticker, an actual signature, handwritten address, etc. The point here is to be human-
        we're all happier to receive a thoughtful message, even if we disagree with it, rather than a machined blanket statement we may otherwise totally agree on.
    </li>
    <li>Don't get upset if you don't hear back. Chances are that you won't get very many letters back - and that's okay. If you want, consider adding an email address or phone
        number (you can get a google voice number free and make a new email address
        just for this if you want) We have a few documented instances where people have been moved to investigate Yang and even all-out caucus by the letters they were sent but
        never wrote back. That's what we're asking them to do, and it's great that they took the message and ran with it! Here's one Redditor's thoughts on it:
        <a href="https://www.reddit.com/r/YangForPresidentHQ/comments/ei7nf3/i_think_i_like_yang">https://www.reddit.com/r/YangForPresidentHQ/comments/ei7nf3/i_think_i_like_yang</a>
    </li>
    <li>Stick to print rather than cursive handwriting, at least for the body of your message. We all know that cursive is beautiful when done well, but it's still hard to read for
        many people. Keep in mind that they need to be able to read what you're saying! Slow down and take as long as you need to write clearly.
    </li>
</ol>
